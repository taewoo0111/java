package test.main;
/*
 * 3. 문자형 (char)
 * 
 * 65536 가지의 코드 값을 가질 수 있다.
 * 전 세계에서 사하는 모든 문자 1글자를 표현할 수 있다.
 * single quotation 을 이용해서 만든다.
 */
public class MainClass04 {
	public static void main(String[] args) {
		char ch1 = 'a';
		char ch2 = 'b';
		char ch3 = '가';
		char ch4 = '@';
		
		//정수 값에 1:1 대응되는 char 값이 존재한다.
		int code1 = ch1;
		int code2 = ch2;
		int code3 = ch3;
		int code4 = ch4;
	}
}
